export function initContact(data: CVData): void {
  const section = document.getElementById('contact')!;

  const email = data.profile.find((p) => p.Type === 'email');
  const websites = data.profile.filter((p) => p.Type === 'website' && p.Icon !== 'globe');

  // Extract email address from link
  const emailMatch = email?.Link?.match(/mailto:([^)\]]+)/);
  const emailAddr = emailMatch ? emailMatch[1] : 'brian_schilder@alumni.brown.edu';

  section.innerHTML = `
    <h2 class="section__title">Contact</h2>
    <div class="contact__email">
      <a href="mailto:${emailAddr}">${emailAddr.replace('@', ' [at] ')}</a>
    </div>
    <div class="contact__links">
      ${websites.map((w) => {
        const icon = ICON_MAP[w.Icon] || ICON_MAP['globe'];
        // Extract URL from markdown link if needed
        const linkMatch = w.Link?.match(/\(([^)]+)\)/);
        const url = linkMatch ? linkMatch[1] : (w.Link || '#');
        return `
          <a href="${url}" class="contact__link" target="_blank" rel="noopener noreferrer" aria-label="${w.Text}">
            <span class="contact__link-icon">${icon}</span>
            <span class="contact__link-label">${w.Text}</span>
          </a>
        `;
      }).join('')}
    </div>
  `;
}
